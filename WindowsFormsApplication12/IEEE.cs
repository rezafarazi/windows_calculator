using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace WindowsFormsApplication12
{
    class IEEE
    {
        public float jam(float a, float b)
        {
            return a + b;
        }
        public float taf(float a, float b)
        {
            return a - b;
        }
        public float zar(float a, float b)
        {
            return a * b;
        }
        public float taq(float a, float b)
        {
            return a / b;
        }
    }
}
